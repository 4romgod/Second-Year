    public void populateTable(){
        double totalDue = 0;
        
        for(int i=0; i<HomePage.saleArray.size(); i++){
            Sale saleItem = HomePage.saleArray.get(i);
            
            modelTableSale.insertRow(modelTableSale.getRowCount(), new Object[]{saleItem.item.getFoodItem(), 1, saleItem.item.getPrice()});
            
            totalDue += saleItem.getItem().getPrice();
            
        }       //end big for loop
        
        totalTxt.setText(totalDue+"");
    
    }       //end popolateTable()
    

